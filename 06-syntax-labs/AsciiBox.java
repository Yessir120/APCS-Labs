public class AsciiBox
{
    public static void main(String[] args)
    {
        System.out.println("Jason Zhou \t  8/30/2021 \n\n" );
        System.out.println("+++++++++++++++++++++++++ " );

        System.out.println("\n\n\n Jason 8/30/2021");
        for(int j = 0; j < 4; j++){
            for(int i = 0; i < 3; i++){
                System.out.println("+++++++++++++++++++++++++");
            }
            for(int i = 0; i < 2; i++){
                System.out.println("AAAAAAAAAAAAAAAAAAAAAAAAA");
            }
        }
        for(int i = 0; i < 3; i++){
            System.out.println("+++++++++++++++++++++++++");
        }
    }
}